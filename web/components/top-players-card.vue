<template>
  <b-card
    :loading="leaderboard == null"
    :title="$t('best.players.long')"
  >
    <b-scrolling-list
      slot="content"
      :items="leaderboard != undefined ? leaderboard : []"
      :cell-columns="2"
      :render-at-least="5"
      key-id="tag"
      render-placeholder
    >
      <template v-slot:item="player">
        <b-card
          :title="player.name"
          :link="localePath(`/profile/${player.tag.replace('#', '')}`)"
          :icon="`/avatars/${player.icon}`"
          :icon-alt="player.name"
          :elevation="elevation"
          class="whitespace-nowrap"
          dense
        >
          <template v-slot:icon="data">
            <media-img-icon v-bind="data"></media-img-icon>
          </template>

          <b-kv-table
            slot="content"
            :rows="[{
              title: $t('metric.trophies'),
              key: 'trophies',
            }]"
            :data="player"
            id-key="tag"
            class="mt-2"
          ></b-kv-table>
        </b-card>
      </template>
    </b-scrolling-list>

    <b-button
      slot="actions"
      :to="localePath(`/leaderboard/trophies`)"
      primary
      sm
    >
      {{ $t('action.open.leaderboard.metric', { metric: $t('metric.trophies') }) }}
    </b-button>
  </b-card>
</template>

<script lang="ts">
import { defineComponent, useAsync, useContext } from '@nuxtjs/composition-api'
import { BScrollingList, BKvTable } from '@schneefux/klicker/components'
import { PlayerRanking } from '~/model/Brawlstars'

export default defineComponent({
  components: {
    BScrollingList,
    BKvTable,
  },
  props: {
    limit: {
      type: Number,
      default: 5
    },
    elevation: {
      type: Number,
      default: 2
    },
  },
  setup(props) {
    const { $api } = useContext()

    const leaderboard = useAsync(async () => {
      const data = await $api.query('rankings.playersByCountry', {
        country: 'global',
      }).catch(() => [])

      return data
        .slice(0, props.limit)
        .map(e => ({
          tag: e.tag,
          name: e.name,
          icon: e.icon.id,
          trophies: e.trophies,
        }))
    }, 'top-players')

    return {
      leaderboard,
    }
  },
})
</script>
